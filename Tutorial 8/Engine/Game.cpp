#include <random>

#include "MainWindow.h"
#include "Game.h"

Game::Game(MainWindow& wnd)
	:
	wnd(wnd),
	gfx(wnd)
{
	InitEnemies();
}

void Game::Go()
{
	gfx.BeginFrame();
	UpdateModel();
	ComposeFrame();
	gfx.EndFrame();
}

void Game::UpdateModel()
{
	HandleInput();
	SetEnemyTrajectory(enemy1X, enemy1Y, enemy1VelocityX, enemy1VelocityY);
	SetEnemyTrajectory(enemy2X, enemy2Y, enemy2VelocityX, enemy2VelocityY);
	SetEnemyTrajectory(enemy3X, enemy3Y, enemy3VelocityX, enemy3VelocityY);

	enemy1Dead = enemy1Dead ? true :
		HaveCollided(heroX, heroY, heroLength, enemy1X, enemy1Y, enemyLength);
	enemy2Dead = enemy2Dead ? true :
		HaveCollided(heroX, heroY, heroLength, enemy2X, enemy2Y, enemyLength);
	enemy3Dead = enemy3Dead ? true :
		HaveCollided(heroX, heroY, heroLength, enemy3X, enemy3Y, enemyLength);
}

void Game::ComposeFrame()
{
	DrawHero(heroX, heroY);
	DrawEnemy(enemy1X, enemy1Y, enemy1Dead);
	DrawEnemy(enemy2X, enemy2Y, enemy2Dead);
	DrawEnemy(enemy3X, enemy3Y, enemy3Dead);
}

void Game::InitEnemies() {
	std::random_device rd;
	std::mt19937 rng(rd());
	std::uniform_int_distribution<int> xDist(0, gfx.ScreenWidth - maxLength);
	std::uniform_int_distribution<int> yDist(0, gfx.ScreenHeight - maxLength);
	enemy1X = xDist(rng);
	enemy1Y = yDist(rng);
	enemy2X = xDist(rng);
	enemy2Y = yDist(rng);
	enemy3X = xDist(rng);
	enemy3Y = yDist(rng);
}

void Game::HandleInput() {
	if (wnd.kbd.KeyIsPressed(VK_UP)) {
		heroY -= heroAcceleration;

		if (heroY <= 0) {
			heroY = 1;
		}
	}

	if (wnd.kbd.KeyIsPressed(VK_DOWN)) {
		heroY += heroAcceleration;

		if (heroY >= gfx.ScreenHeight - heroHeightBuffer) {
			heroY = gfx.ScreenHeight - heroHeightBuffer;
		}
	}

	if (wnd.kbd.KeyIsPressed(VK_LEFT)) {
		heroX -= heroAcceleration;

		if (heroX <= 0) {
			heroX = 1;
		}
	}

	if (wnd.kbd.KeyIsPressed(VK_RIGHT)) {
		heroX += heroAcceleration;

		if (heroX >= gfx.ScreenWidth - heroWidthBuffer) {
			heroX = gfx.ScreenWidth - heroWidthBuffer;
		}
	}
}

void Game::SetEnemyTrajectory(int& x, int& y, int& velocityX, int& velocityY) {
	if (y <= enemyHeightBuffer) {
		velocityY = -velocityY;
	}

	if (y >= gfx.ScreenHeight - enemyHeightBuffer) {
		velocityY = -velocityY;
	}

	if (x <= enemyWidthBuffer) {
		velocityX = -velocityX;
	}

	if (x >= gfx.ScreenWidth - enemyWidthBuffer) {
		velocityX = -velocityX;
	}

	x += velocityX;
	y += velocityY;
}

bool Game::HaveCollided(int x1, int y1, int length1, int x2, int y2, int length2) {
	const int left1 = x1;
	const int left2 = x2;
	const int top1 = y1;
	const int top2 = y2;
	const int right1 = left1 + length1;
	const int right2 = left2 + length2;
	const int bottom1 = top1 + length1;
	const int bottom2 = top2 + length2;

	return
		left1 <= right2 &&
		right1 >= left2 &&
		top1 <= bottom2 &&
		bottom1 >= top2;
}

